# str ou string ou texto
# São textos que estão dentro de aspas

# Aspas simples
print('Texto com aspas simples...')

# Aspas duplas
print("Texto com aspas duplas...")

# Aspas dentro de aspas
print("Meu nome é 'Helton'")
print('Sou casado com "Rosilene"')

# Escape
print("Meu sobrenome é \"Barbosa\"")

# r
print(r"Helton \"Lampião\"")


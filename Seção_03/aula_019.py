# Os operadores relacionais em Python são usados para comparar valores e
# produzir resultados booleanos (verdadeiro ou falso) com base nas relações
# entre esses valores.

# Aqui estão os principais operadores relacionais em Python:

# 1. Igualdade (==)
# Verifica se dois valores são iguais
a = 5
b = 5
print('\n1. Igualdade (==)')
print(a == b)  # Saída: True
texto1 = "python"
texto2 = "Python"
print(texto1 == texto2)  # Saída: False

# 2. Diferença (!=)
# Verifica se dois valores são diferentes
x = 10
y = 20
print('\n2. Diferença (!=)')
print(x != y)  # Saída: True
nome1 = "Maria"
nome2 = "João"
print(nome1 != nome2)  # Saída: True

# 3. Maior que (>)
# Verifica se o valor da esquerda é maior que o valor da direita
valor1 = 15
valor2 = 10
print('\n3. Maior que (>)')
print(valor1 > valor2)  # Saída: True
numero1 = 3.5
numero2 = 2
print(numero1 > numero2)  # Saída: True

# 4. Menor que (<)
# Verifica se o valor da esquerda é menor que o valor da direita
print('\n4. Menor que (<)')
idade1 = 25
idade2 = 30
print(idade1 < idade2)  # Saída: True
numero1 = 2.5
numero2 = 5
print(numero1 < numero2)  # Saída: True

# 5. Maior ou igual que (>=)
# Verifica se o valor da esquerda é maior ou igual ao valor da direita
a = 10
b = 5
print('\n5. Maior ou igual que (>=)')
print(a >= b)  # Saída: True
valor1 = 10
valor2 = 10
print(valor1 >= valor2)  # Saída: True

# 6. Menor ou igual que (<=)
# Verifica se o valor da esquerda é menor ou igual ao valor da direita
x = 5
y = 10
print('\n6. Menor ou igual que (<=)')
print(x <= y)  # Saída: True
numero1 = 8
numero2 = 8
print(numero1 <= numero2)  # Saída: True

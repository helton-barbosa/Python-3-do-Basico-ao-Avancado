# Tipos de dados (int e float)
#
# int - Número inteiro. Representa qualquer número positivo e negativo.
# int sem sinal representa um númro inteiro positivo.
print(1000) # inteiro positivo
print(-123) # inteiro negativo

# float - Número com ponto flutuante. O tipo float representa qualquer
# número com vírgula, tanto positivo ou negativo. float sem sinal indica
# número com ponto flutuante positivo.
print(3.14) # float positivo
print(-99.9) # float negativo

# A função type() exibe o tipo do objeto.
print(type(1))
print(type(1.10))


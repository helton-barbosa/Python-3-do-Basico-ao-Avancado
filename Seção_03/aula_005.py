# Tipo de dado bool (booleano)
# Ao questionar algo em um programa, só existem duas respostas possíveis:
# sim (True) e não (False).
# Existem vários operadores para questionar.
# Dentre eles o ==, que é um operador lógico que questiona
# se um valor é igual a outro valor
print(10 == 10)  # Sim -> True (verdadeiro)
print(1 == 2)  # Não -> False (falso)
